function WaveLayer({ id, rowCount }: { id: string; rowCount: number }) {
  const rows = []
  for (let i = 0; i < rowCount; i++) {
    const pi = i % paths.length
    const wi = (i * 7 + (id === "2" ? 13 : 0)) % weights.length
    const ci = (i + (id === "2" ? 3 : 0)) % blueColors.length
    const sw = weights[wi]
    const op = 0.18 + (sw / 3.2) * 0.14
    rows.push(
      <svg
        class="dw-bg-row"
        viewBox="0 0 1400 22"
        preserveAspectRatio="none"
        xmlns="http://www.w3.org/2000/svg"
      >
        <path
          d={paths[pi]}
          fill="none"
          stroke={blueColors[ci]}
          stroke-width={sw}
          stroke-linecap="round"
          stroke-linejoin="round"
          opacity={op}
        />
      </svg>
    )
  }

  return (
    <div class={`dw-bg-layer dw-bg-layer-${id}`}>
      {rows}
    </div>
  )
}

